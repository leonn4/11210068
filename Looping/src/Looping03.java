
public class Looping03 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int Angka =1 ;
			while (Angka <= 6){
				if (Angka < 6) {
					System.out.println(Angka + " Kalah");
				} else {
					System.out.println(Angka + " Horeee !");
				}
				Angka = Angka + 1;
			}

	}

}
